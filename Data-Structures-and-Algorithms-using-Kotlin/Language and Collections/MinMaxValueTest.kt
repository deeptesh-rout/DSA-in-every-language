
fun main() {

    val maxByte = java.lang.Byte.MAX_VALUE
    val minByte = java.lang.Byte.MIN_VALUE

    val maxShort = java.lang.Short.MAX_VALUE
    val minShort = java.lang.Short.MIN_VALUE

    val maxInteger = Integer.MAX_VALUE
    val minInteger = Integer.MIN_VALUE

    val maxLong = java.lang.Long.MAX_VALUE
    val minLong = java.lang.Long.MIN_VALUE

    val maxFloat = java.lang.Float.MAX_VALUE
    val minFloat = java.lang.Float.MIN_VALUE

    val maxDouble = java.lang.Double.MAX_VALUE
    val minDouble = java.lang.Double.MIN_VALUE

    println("Range of byte :: $minByte to $maxByte.")
    println("Range of short :: $minShort to $maxShort.")
    println("Range of integer :: $minInteger to $maxInteger.")
    println("Range of long :: $minLong to $maxLong.")
    println("Range of float :: $minFloat to $maxFloat.")
    println("Range of double :: $minDouble to $maxDouble.")
}
